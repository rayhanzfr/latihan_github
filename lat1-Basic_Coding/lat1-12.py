#Perbandingan rasio umur Ayah dan Andi adalah 5 : 2. Jika jumlah umur keduanya adalah 49 tahun, 
# berapakah umur dari:a) Ayah? b) Andi?


ayah= int(input('Masukan Rasio Umur Ayah ='))
andi= int(input('Masukan Rasio Umur Andi ='))
total=int(input('Masukan Total Umur Ayah dan Andi ='))
n=total/(ayah+andi)
Tayah=ayah*n
Tandi=andi*n
print ('Ayah =',Tayah)
print ('Andi =',Tandi)
